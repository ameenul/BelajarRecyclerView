package com.example.belajarrecyclerview;

public class Kontak {

    private String nama;
    private String hp;

    public Kontak(String nama, String hp) {
        this.nama = nama;
        this.hp = hp;
    }

    public String getNama() {
        return nama;
    }

    public String getHp() {
        return hp;
    }





}
